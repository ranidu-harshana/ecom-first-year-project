function setSource(){
    var src = sessionStorage.getItem('src');
    var id = sessionStorage.getItem('id');
    var index = cimages.indexOf(id)
    document.getElementById('iname').innerHTML = details[index][0];
    document.getElementById('idesc').innerHTML = details[index][1];
    document.getElementById('iprice').innerHTML = details[index][2];
    document.getElementById('itrans').innerHTML = details[index][3];
    document.cimage.src = src;
    document.image1.src = src;
    document.image2.src = details[index][4][0];
    document.image3.src = details[index][4][1];
    document.image4.src = details[index][4][2];
    setTotPrice();
}
window.onload = setSource;

function changeMainImage(name){
    var image_src = document.getElementsByName(name)[0].getAttribute("src");
    document.getElementById('main_image').setAttribute("src", image_src);
}

function increment(){
    //documnet.getElementById('n').innerHTML = "text";
    var quantity = document.getElementById('quantity_input').getAttribute("value");
    if(quantity >= 1){
        quantity++;
        var price = document.getElementById('iprice').textContent;
        var nprice = parseInt(price.substring(3,price.length)) * quantity;
        document.getElementById('tot_price').innerHTML = "Rs. ".concat(nprice.toString());
        document.getElementById('quantity_input').setAttribute("value",quantity);
    }
} 

function decrement(){
    var quantity = document.getElementById('quantity_input').getAttribute("value");
    if (quantity != 1) {
        quantity--;
        var price = document.getElementById('iprice').textContent;
        var tprice = document.getElementById('tot_price').textContent;
        var nprice = parseInt(price.substring(3,price.length));
        var t_price = parseInt(tprice.substring(3,tprice.length)) - nprice;
        document.getElementById('tot_price').innerHTML = "Rs. ".concat(t_price.toString());
        document.getElementById('quantity_input').setAttribute("value",quantity);
    }
}

function setTotPrice(){
    var price = document.getElementById('iprice').textContent;
    document.getElementById('tot_price').innerHTML = price;
}
